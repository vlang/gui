import gui

// Fonts
// =============================
// Displays the default Gui font in all its glory...

@[heap]
struct FontsApp {
pub mut:
	selected_family string = 'Normal'
	selected_font   string = '1'
	light_theme     bool
}

fn main() {
	mut window := gui.window(
		title:   'Gui Fonts'
		state:   &FontsApp{}
		width:   800
		height:  600
		on_init: fn (mut w gui.Window) {
			w.update_view(main_view)
		}
	)
	window.set_theme(gui.theme_dark_bordered)
	window.run()
}

fn main_view(mut window gui.Window) &gui.View {
	w, h := window.window_size()
	return gui.row(
		width:   w
		height:  h
		sizing:  gui.fixed_fixed
		content: [
			side_panel(mut window),
			font_panel(window),
		]
	)
}

fn side_panel(mut window gui.Window) &gui.View {
	mut app := window.state[FontsApp]()
	prefix := match app.selected_family {
		'Bold' { 'b' }
		'Italic' { 'i' }
		'Mono' { 'm' }
		else { 'n' }
	}
	return gui.column(
		sizing:  gui.fit_fill
		spacing: gui.theme().spacing_large * 2
		content: [
			gui.text(text: 'Glyphs: ${code_points.len.str()}'),
			gui.radio_button_group_column(
				title:     ' Family '
				sizing:    gui.fill_fill
				value:     app.selected_family
				options:   [
					gui.radio_option('Normal', 'Normal'),
					gui.radio_option('Bold', 'Bold'),
					gui.radio_option('Italic', 'Italic'),
					gui.radio_option('Mono', 'Mono'),
				]
				on_select: fn [mut app] (value string, mut _ gui.Window) {
					app.selected_family = value
				}
			),
			gui.radio_button_group_column(
				title:     ' ${app.selected_family} '
				sizing:    gui.fill_fill
				value:     app.selected_font
				options:   [
					gui.radio_option('${prefix}1', '1'),
					gui.radio_option('${prefix}2', '2'),
					gui.radio_option('${prefix}3', '3'),
					gui.radio_option('${prefix}4', '4'),
					gui.radio_option('${prefix}5', '6'),
					gui.radio_option('${prefix}6', '6'),
				]
				on_select: fn [mut app] (value string, mut _ gui.Window) {
					app.selected_font = value
				}
			),
			gui.row(sizing: gui.fill_fill),
			toggle_theme(app),
		]
	)
}

fn font_panel(window &gui.Window) &gui.View {
	app := window.state[FontsApp]()
	mut rows := []&gui.View{}
	mut cols := []&gui.View{}
	width := f32(gui.theme().n3.size) * 1.5

	text_style := match app.selected_family {
		'Bold' {
			match app.selected_font {
				'2' { gui.theme().b2 }
				'3' { gui.theme().b3 }
				'4' { gui.theme().b4 }
				'5' { gui.theme().b5 }
				'6' { gui.theme().b6 }
				else { gui.theme().b1 }
			}
		}
		'Italic' {
			match app.selected_font {
				'2' { gui.theme().i2 }
				'3' { gui.theme().i3 }
				'4' { gui.theme().i4 }
				'5' { gui.theme().i5 }
				'6' { gui.theme().i6 }
				else { gui.theme().i1 }
			}
		}
		'Mono' {
			match app.selected_font {
				'2' { gui.theme().m2 }
				'3' { gui.theme().m3 }
				'4' { gui.theme().m4 }
				'5' { gui.theme().m5 }
				'6' { gui.theme().m6 }
				else { gui.theme().m1 }
			}
		}
		else {
			match app.selected_font {
				'2' { gui.theme().n2 }
				'3' { gui.theme().n3 }
				'4' { gui.theme().n4 }
				'5' { gui.theme().n5 }
				'6' { gui.theme().n6 }
				else { gui.theme().n1 }
			}
		}
	}

	for i, cp in code_points {
		if i % 18 == 0 && i != 0 {
			rows << gui.row(padding: gui.padding_none, content: cols)
			cols.clear()
		}
		cols << gui.text(text: cp, min_width: width, text_style: text_style)
	}

	return gui.column(
		id_scroll: 1
		fill:      true
		color:     gui.theme().color_panel
		sizing:    gui.fill_fill
		content:   rows
	)
}

fn toggle_theme(app &FontsApp) &gui.View {
	return gui.row(
		h_align: .end
		padding: gui.padding_none
		content: [
			gui.toggle(
				text_select:   gui.icon_moon
				text_unselect: gui.icon_sunny_o
				text_style:    gui.theme().icon3
				padding:       gui.theme().padding_small
				select:        app.light_theme
				on_click:      fn (_ &gui.ToggleCfg, mut _ gui.Event, mut w gui.Window) {
					mut app := w.state[FontsApp]()
					app.light_theme = !app.light_theme
					theme := if app.light_theme {
						gui.theme_light_bordered
					} else {
						gui.theme_dark_bordered
					}
					w.set_theme(theme)
				}
			),
		]
	)
}

